uint16_t gray_to_rgb565(uint8_t gray) {
    uint8_t r5 = gray >> 3;
    uint8_t g6 = gray >> 2;
    uint8_t b5 = gray >> 3;
    return (r5 << 11) | (g6 << 5) | b5;
}

